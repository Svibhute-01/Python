
fruits = ["apple", "banana", "cherry", "date", "elderberry"]
text = "PythonProgramming"


print("List:", fruits)
print("String:", text)


print("\n--- Positive Indexing ---")
print("fruits[0]:", fruits[0])      
print("fruits[2]:", fruits[2])      
print("text[1]:", text[1])         
print("text[4]:", text[4])          


print("\n--- Negative Indexing ---")
print("fruits[-1]:", fruits[-1])    
print("fruits[-3]:", fruits[-3])    
print("text[-1]:", text[-1])       
print("text[-4]:", text[-4])        

print("\n--- Slicing ---")


print("fruits[1:4]:", fruits[1:4])       
print("fruits[:3]:", fruits[:3])         
print("fruits[2:]:", fruits[2:])         
print("fruits[-4:-1]:", fruits[-4:-1])   


print("text[0:6]:", text[0:6])           
print("text[6:]:", text[6:])             
print("text[-11:-4]:", text[-11:-4])    
print("text[::-1]:", text[::-1])         
